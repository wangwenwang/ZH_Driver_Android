package com.kaidongyuan.app.zhdriver.bean;

public class Tools{

	private int logo;

	private String name;

	public int getLogo() {
		return logo;
	}

	public void setLogo(int logo) {
		this.logo = logo;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public Tools(int logo, String name) {
		super();
		this.logo = logo;
		this.name = name;
	}
	
	
}
